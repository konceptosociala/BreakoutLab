/**
 * The GameState enum represents the different states of the Breakout game.
 * It includes constants for the main menu, level selection, various game levels,
 * game over, you win, and exit states.
 */
public enum GameState {
	MainMenu,
	SelectLevel,
	Level1,
	Level2,
	Level3,
	GameOver,
	YouWin,
	Exit,
}
